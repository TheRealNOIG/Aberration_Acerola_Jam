const WINDOW_WIDTH: usize = 1080;
const WINDOW_HEIGHT: usize = 720;
const BUFFER_WIDTH: usize = 180;
const BUFFER_HEIGHT: usize = 100;
const FOV: f32 = PI / 3.0;

fn main() {
    let mut buffer: Vec<u32> = vec![0; BUFFER_WIDTH * BUFFER_HEIGHT];
    let mut portal_buffer: Vec<u32> = vec![0; BUFFER_WIDTH * BUFFER_HEIGHT];
    let mut cmp_buffer: Vec<u32> = vec![0; BUFFER_WIDTH * BUFFER_HEIGHT];

    let mut render_state = RenderState {
        debug_render: true,
        color_state: false,
    };

    let window_handler = Rc::new(RefCell::new(
        Window::new(
            "Test - ESC to exit",
            WINDOW_WIDTH,
            WINDOW_HEIGHT,
            WindowOptions::default(),
        )
        .unwrap_or_else(|e| {
            panic!("{}", e);
        }),
    ));
    let window_binding = Rc::clone(&window_handler);
    let window_binding_clone = Rc::clone(&window_handler);

    let mut fps_timer = Timer::new(
        1000,
        Box::new(move |ticks: usize| {
            window_binding_clone
                .borrow_mut()
                .set_title(&format!("FPS: {}", ticks));
        }),
    );

    let mut player = Player::new(4.0, 4.0, 0.0);

    while window_binding.borrow().is_open() && !window_binding.borrow().is_key_down(Key::Escape) {
        handle_input(
            &mut render_state,
            &mut buffer,
            &mut window_binding.borrow_mut(),
        );

        move_player(&mut player, &window_binding.borrow());

        if render_state.debug_render {
            render_black_and_white(&mut buffer, &player);
        } else {
            render_aberration(&mut buffer, &player);
        }
        draw_portal(&mut portal_buffer, &player, &render_state);

        for i in 0..portal_buffer.len() {
            if portal_buffer[i] & 0xFF000000 == 0xFF000000 {
                cmp_buffer[i] = portal_buffer[i];
            } else {
                cmp_buffer[i] = buffer[i];
            }
        }

        let scalled_buffer = scale_buffer(
            &cmp_buffer,
            BUFFER_WIDTH,
            BUFFER_HEIGHT,
            WINDOW_WIDTH,
            WINDOW_HEIGHT,
        );

        window_binding
            .borrow_mut()
            .update_with_buffer(&scalled_buffer, WINDOW_WIDTH, WINDOW_HEIGHT)
            .unwrap();
        fps_timer.tick();
    }
}
